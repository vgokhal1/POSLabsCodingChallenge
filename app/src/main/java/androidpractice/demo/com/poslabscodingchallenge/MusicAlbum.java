package androidpractice.demo.com.poslabscodingchallenge;

public class MusicAlbum {

    public String getmArtistName() {
        return mArtistName;
    }

    public void setmArtistName(String mArtistName) {
        this.mArtistName = mArtistName;
    }

    public String getmAlbumName() {
        return mAlbumName;
    }

    public void setmAlbumName(String mAlbumName) {
        this.mAlbumName = mAlbumName;
    }

    public String getmSongName() {
        return mSongName;
    }

    public void setmSongName(String mSongName) {
        this.mSongName = mSongName;
    }

    public String getmSongPreviewURL() {
        return mSongPreviewURL;
    }

    public void setmSongPreviewURL(String mSongPreviewURL) {
        this.mSongPreviewURL = mSongPreviewURL;
    }

    public String getmAlbumPhotoURL() {
        return mAlbumPhotoURL;
    }

    public void setmAlbumPhotoURL(String mAlbumPhotoURL) {
        this.mAlbumPhotoURL = mAlbumPhotoURL;
    }

    public String getmCountry() {
        return mCountry;
    }

    public void setmCountry(String mCountry) {
        this.mCountry = mCountry;
    }

    public Double getmTrackPrice() {
        return mTrackPrice;
    }

    public void setmTrackPrice(Double mTrackPrice) {
        this.mTrackPrice = mTrackPrice;
    }


    public String getmCurrency() {
        return mCurrency;
    }

    public void setmCurrency(String mCurrency) {
        this.mCurrency = mCurrency;
    }


    private String mArtistName;
    private String mAlbumName;
    private String mSongName;
    private String mSongPreviewURL;
    private String mAlbumPhotoURL;
    private String mCountry;
    private Double mTrackPrice;
    private String mCurrency;
}
